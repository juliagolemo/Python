def begruessung(name):
    print(f"Hallo meine Liebe {name}")

# To jest funkcja o nazwie begruessung, która przyjmuje jeden argument — name (czyli imię, które podasz).
# Funkcja ta po prostu wypisuje powitanie w formie "Hallo meine Liebe" (co po niemiecku oznacza "Cześć moja droga") oraz dodaje imię, które zostało przekazane do funkcji.

